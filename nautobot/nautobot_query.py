def load_config():
    """Read URL and token from nautobotApi.cfg."""
    if not os.path.exists(CONFIG_FILE):
        print(f"\n[-] ERROR: {CONFIG_FILE} not found.")
        print("    Run nautobot_setup.py first to generate your token.")
        sys.exit(1)

    config = configparser.ConfigParser()
    config.read(CONFIG_FILE)

    try:
        url   = config["nautobot"]["url"].rstrip("/")
        token = config["nautobot"]["token"]
        return url, token
    except KeyError:
        print(f"\n[-] ERROR: {CONFIG_FILE} is missing required fields.")
        print("    Re-run nautobot_setup.py to regenerate it.")
        sys.exit(1)


