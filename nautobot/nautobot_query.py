def search_devices_by_names(url, token, terms, match_param):
    """
    Shared lookup engine for Quick Search and File Search.

    Exact match   — shows full device detail immediately.
    Partial match — shows numbered summary list, then offers drill-down.

    Returns flat list of CSV-ready rows across all matched devices.
    """
    all_csv_rows = []
    found_count  = 0
    missing      = []
    is_exact     = match_param == "name"

    for term in terms:
        term = term.strip()
        if not term:
            continue

        print(f"\n[*] Searching: '{term}' ...")

        matched = api_get(url, token, "/api/dcim/devices/",
                          params={match_param: term, "limit": 0})

        if not matched:
            missing.append(term)
            print(f"  [!] No devices found for: '{term}'")
            continue

        found_count += len(matched)

        # Exact — full detail immediately
        if is_exact:
            rows = display_device_detail(url, token, matched[0])
            all_csv_rows.extend(rows)

        # Partial — summary list with drill-down
        else:
            summary_rows = display_device_summary(matched, term)

            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            prompt_save_csv(
                rows             = summary_rows,
                fieldnames       = SUMMARY_FIELDS,
                default_filename = f"search_{term}_{timestamp}.csv",
            )

            while True:
                print(f"\n  Enter a number to drill into a device,")
                drill = input(
                    "  or press Enter to continue to next term: "
                ).strip()

                if not drill:
                    break

                if not drill.isdigit() or \
                   not (1 <= int(drill) <= len(matched)):
                    print(f"\n  [!] Invalid — enter a number between "
                          f"1 and {len(matched)}.")
                    continue

                selected    = matched[int(drill) - 1]
                device_name = selected.get("name", "N/A")
                print()
                detail_rows = display_device_detail(url, token, selected)
                all_csv_rows.extend(detail_rows)

                timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
                prompt_save_csv(
                    rows             = detail_rows,
                    fieldnames       = DEVICE_DETAIL_FIELDS,
                    default_filename = f"device_{device_name}_{timestamp}.csv",
                )

                input("\n  Press Enter to return to match list...")
                display_device_summary(matched, term)

    divider()
    print(f"  Search complete.")
    print(f"  Matched : {found_count} device(s)")
    if missing:
        print(f"  No results for {len(missing)} term(s):")
        for m in missing:
            print(f"            - {m}")
    divider()

    return all_csv_rows


def query_search_by_name(url, token):
    """
    Targeted device search with match type selection.
    Quick input (comma separated) or File input (one per line).
    """
    while True:
        print("\n")
        divider()
        print("  Search by Device Name (Targeted)")
        divider()
        print("  1. Quick Search  — paste terms (comma separated)")
        print("  2. File Search   — load from text file (one per line)")
        print("  3. Back to main menu")
        divider()

        choice = input("  Select an option: ").strip()

        if choice == "1":
            print("\n  Paste search terms separated by commas.")
            raw = input("  Terms: ").strip()

            if not raw:
                print("  [!] No input provided.")
                continue

            terms = [t.strip() for t in raw.split(",") if t.strip()]

            if not terms:
                print("  [!] No valid terms found.")
                continue

            match_param, match_label = prompt_match_type()

            print(f"\n[*] Quick Search — {len(terms)} term(s) "
                  f"using '{match_label}' matching.")

            all_csv_rows = search_devices_by_names(
                url, token, terms, match_param
            )

            if all_csv_rows:
                timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
                prompt_save_csv(
                    rows             = all_csv_rows,
                    fieldnames       = DEVICE_DETAIL_FIELDS,
                    default_filename = f"search_results_{timestamp}.csv",
                )

            input("\n  Press Enter to return to search menu...")

        elif choice == "2":
            print("\n  Enter the full path to your search terms file.")
            print("  File format: one term per line, # lines are ignored.")
            filepath = input("  File path: ").strip().strip('"')

            if not filepath:
                print("  [!] No file path provided.")
                continue

            if not os.path.exists(filepath):
                print(f"  [-] File not found: {filepath}")
                print("      Check the path and try again.")
                continue

            try:
                with open(filepath, "r", encoding="utf-8") as f:
                    terms = [
                        line.strip()
                        for line in f.readlines()
                        if line.strip()
                        and not line.startswith("#")
                    ]
            except IOError as e:
                print(f"  [-] ERROR reading file: {e}")
                continue

            if not terms:
                print("  [!] File is empty or contains no valid terms.")
                continue

            match_param, match_label = prompt_match_type()

            print(f"\n[*] File Search — {len(terms)} term(s) from "
                  f"'{os.path.basename(filepath)}' "
                  f"using '{match_label}' matching.")

            all_csv_rows = search_devices_by_names(
                url, token, terms, match_param
            )

            if all_csv_rows:
                timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
                prompt_save_csv(
                    rows             = all_csv_rows,
                    fieldnames       = DEVICE_DETAIL_FIELDS,
                    default_filename = f"search_results_{timestamp}.csv",
                )

            input("\n  Press Enter to return to search menu...")

        elif choice == "3":
            return

        else:
            print("\n  [!] Invalid selection. Enter 1, 2, or 3.")


