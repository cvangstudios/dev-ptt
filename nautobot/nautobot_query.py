def query_devices(url, token):
    """
    Stage 1 — Single API call pulls all devices at SITE_NAME.
              Displays a fast numbered summary list.
              CSV export available from summary.
    Stage 2 — User selects a number to drill into full device detail.
              Additional API calls made only for the selected device.
              CSV export available from detail view.
              Returns to summary list after detail — not main menu.
    """
    while True:
        print(f"\n[*] Fetching all devices at site: {SITE_NAME} ...")

        devices = api_get(url, token, "/api/dcim/devices/",
                          params={"site": SITE_NAME, "limit": 0})

        if not devices:
            print(f"[!] No devices found for site: {SITE_NAME}")
            print("    Check that SITE_NAME slug is correct (lowercase).")
            input("  Press Enter to return to menu...")
            return

        print(f"[+] Found {len(devices)} device(s).\n")
        divider()
        print(f"  {'#':<5} {'Device Name':<35} {'Status':<12} "
              f"{'Platform':<20} {'Rack':<20} {'RU'}")
        divider("-")

        # Build summary CSV rows while printing
        summary_csv_rows = []
        for idx, device in enumerate(devices, start=1):
            name       = device.get("name", "N/A")
            status     = device.get("status", {}).get("label", "N/A")
            platform   = device.get("platform")
            platform   = platform.get("name", "N/A") if platform else "N/A"
            rack       = device.get("rack")
            rack_name  = rack.get("name", "N/A") if rack else "Not racked"
            position   = str(device.get("position", "N/A"))
            primary_ip = device.get("primary_ip")
            primary_ip = primary_ip.get("address", "N/A") \
                         if primary_ip else "Not assigned"

            print(f"  {idx:<5} {name:<35} {status:<12} "
                  f"{platform:<20} {rack_name:<20} {position}")

            summary_csv_rows.append({
                "Device Name": name,
                "Status":      status,
                "Platform":    platform,
                "Primary IP":  primary_ip,
                "Rack":        rack_name,
                "RU Position": position,
            })

        divider()

        # CSV export for summary list
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = summary_csv_rows,
            fieldnames       = [
                "Device Name", "Status", "Platform",
                "Primary IP", "Rack", "RU Position"
            ],
            default_filename = f"devices_summary_{SITE_NAME}_{timestamp}.csv",
        )

        # Drill-down prompt
        print("\n  Enter a number for full detail,")
        choice = input("  or press Enter to return to menu: ").strip()

        if not choice:
            return

        if not choice.isdigit() or not (1 <= int(choice) <= len(devices)):
            print(f"\n[!] Invalid selection. Enter a number between "
                  f"1 and {len(devices)}.")
            continue

        selected_device = devices[int(choice) - 1]
        device_name     = selected_device.get("name", "N/A")
        print()
        detail_rows = display_device_detail(url, token, selected_device)

        # CSV export for device detail
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = detail_rows,
            fieldnames       = DEVICE_DETAIL_FIELDS,
            default_filename = f"device_{device_name}_{timestamp}.csv",
        )

        input("\n  Press Enter to return to device list...")


