def build_headers(token):
    """Return standard auth headers for all API calls."""
    return {
        "Authorization": f"Token {token}",
        "Accept":        "application/json",
    }


def api_get(url, token, endpoint, params=None):
    """
    GET request with automatic pagination.
    Returns a flat list of all results across all pages.

    Pass params={"limit": 0} to get all results in a single API call
    with no pagination overhead — preferred for all list views.
    """
    headers     = build_headers(token)
    all_results = []
    next_url    = f"{url}{endpoint}"

    while next_url:
        try:
            response = requests.get(
                next_url,
                headers=headers,
                params=params,
                verify=False,
                timeout=30,
            )
        except requests.exceptions.ConnectionError:
            print(f"\n[-] ERROR: Cannot reach {url}. Check network/URL.")
            return []
        except requests.exceptions.Timeout:
            print(f"\n[-] ERROR: Request timed out.")
            return []

        if response.status_code == 403:
            print(f"\n[!] 403 Forbidden on {endpoint} — check token permissions.")
            return []
        elif response.status_code == 404:
            print(f"\n[!] 404 Not Found: {endpoint} — check the endpoint path.")
            return []
        elif response.status_code != 200:
            print(f"\n[!] Unexpected HTTP {response.status_code} on {endpoint}")
            return []

        data = response.json()

        if "results" in data:
            all_results.extend(data["results"])
            next_url = data.get("next")
            params   = None
        else:
            all_results.append(data)
            break

    return all_results


def test_connection(url, token):
    """Quick connectivity and auth check on startup."""
    print(f"\n[*] Connecting to {url} ...")
    try:
        response = requests.get(
            f"{url}/api/",
            headers=build_headers(token),
            verify=False,
            timeout=10,
        )
        if response.status_code == 200:
            print(f"[+] Connected successfully.\n")
            return True
        else:
            print(f"[-] Connection failed: HTTP {response.status_code}")
            return False
    except requests.exceptions.ConnectionError:
        print(f"[-] ERROR: Cannot reach {url}.")
        return False


