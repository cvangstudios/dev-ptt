def drill_down_loop(url, token, devices, term, default_csv_name):
    """
    Shared drill-down prompt used after every summary list.

    Options:
        number  — drill into that device for full detail
        s       — search again (returns to caller's search loop)
        Enter   — return to Global Search menu

    Args:
        url              : Nautobot base URL
        token            : API token
        devices          : List of device dicts shown in summary
        term             : Search term used (for display and filenames)
        default_csv_name : Suggested filename for detail CSV export

    Returns True  if user wants to search again.
    Returns False if user wants to return to menu.
    """
    while True:
        print("\n  Enter a number to drill into a device,")
        print("  's' to search again, or Enter to return to menu:")
        choice = input("  > ").strip().lower()

        if not choice:
            return False       # back to Global Search menu

        if choice == "s":
            return True        # search again

        if not choice.isdigit() or not (1 <= int(choice) <= len(devices)):
            print(f"\n  [!] Invalid — enter a number between 1 and "
                  f"{len(devices)}, 's' to search again, "
                  f"or Enter to return to menu.")
            continue

        selected    = devices[int(choice) - 1]
        device_name = selected.get("name", "N/A")
        print()
        detail_rows = display_device_detail(url, token, selected)

        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = detail_rows,
            fieldnames       = DEVICE_DETAIL_FIELDS,
            default_filename = f"device_{device_name}_{timestamp}.csv",
        )

        input("\n  Press Enter to return to search results...")

        # Redisplay summary so user doesn't lose their place
        display_device_summary(devices, term)


