        print()

        # CSV export for rack device list
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = rack_csv_rows,
            fieldnames       = [
                "Rack", "Location", "Rack Height", "Rack Status",
                "RU Position", "Device Name", "Device Type", "Status",
            ],
            default_filename = f"rack_{rack_name}_{timestamp}.csv",
        )

        input("\n  Press Enter to return to rack list...")


