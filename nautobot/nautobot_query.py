        devices = api_get(url, token, "/api/dcim/devices/",
                          params={"rack_id": rack_id})

        divider()
        print(f"  RACK : {rack_name}")
        divider("-")
        print(f"  Location  : {rack_location}")
        print(f"  Height    : {rack_height}U")
        print(f"  Status    : {rack_status}")

