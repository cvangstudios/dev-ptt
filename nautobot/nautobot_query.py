def main_menu():
    while True:
        print("\n")
        divider()
        print("  Nautobot Query Tool")
        print(f"  Site: {SITE_NAME}")
        divider()
        print("  1. Global Search")
        print("  2. Search by Device Name")
        print("  3. Exit")
        divider()

        choice = input("  Select an option: ").strip()

        if choice == "1":
            query_global_search(url, token)
        elif choice == "2":
            query_search_by_name(url, token)
        elif choice == "3":
            print("\n  Goodbye.\n")
            sys.exit(0)
        else:
            print("\n  [!] Invalid selection. Enter 1, 2, or 3.")


