# =======================================================

def query_global_search(url, token):
    """
    Search all devices using a contains (partial) match on name.
    Always uses name__ic — case insensitive contains.

    Displays a numbered summary list:
        Name, Status, Tenant, Role, Type, Site, Rack, Primary IP

    Offers:
        - CSV export of summary list
        - Numbered drill-down into any result for full device detail
        - CSV export of device detail
        - Returns to summary list after drill-down
    """
    while True:
        print("\n")
        divider()
        print("  Global Search")
        print("  Searches device names — partial matches always included")
        divider()

        term = input("  Enter search term or press Enter to return to menu: ").strip()

        if not term:
            return

        print(f"\n[*] Searching for devices matching: '{term}' ...")

        devices = api_get(
            url, token,
            "/api/dcim/devices/",
            params={"name__ic": term, "limit": 0},
        )

        if not devices:
            print(f"  [!] No devices found matching: '{term}'")
            input("  Press Enter to search again...")
            continue

        # Display summary list
        summary_rows = display_summary_and_drill(url, token, devices, term)

        # CSV export for summary
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = summary_rows,
            fieldnames       = SUMMARY_FIELDS,
            default_filename = f"global_search_{term}_{timestamp}.csv",
        )

        # Drill-down loop — stays on summary list until user exits
        while True:
            print("\n  Enter a number to drill into a device,")
            print("  's' to search again, or press Enter to return to menu:")
            choice = input("  > ").strip().lower()

            if not choice:
                return

            if choice == "s":
                break   # back to outer search loop

            if not choice.isdigit() or not (1 <= int(choice) <= len(devices)):
                print(f"\n  [!] Invalid — enter a number between 1 "
                      f"and {len(devices)}, 's' to search again, "
                      f"or Enter to return to menu.")
                continue

            selected    = devices[int(choice) - 1]
            device_name = selected.get("name", "N/A")
            print()
            detail_rows = display_device_detail(url, token, selected)

            # CSV export for device detail
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            prompt_save_csv(
                rows             = detail_rows,
                fieldnames       = DEVICE_DETAIL_FIELDS,
                default_filename = f"device_{device_name}_{timestamp}.csv",
            )

            input("\n  Press Enter to return to search results...")

            # Redisplay summary list after returning from detail
            display_summary_and_drill(url, token, devices, term)


