def global_search_by_device_name(url, token):
    """
    Search devices by name using contains (partial) matching.
    Filter: name__ic=term
    """
    while True:
        print("\n")
        divider()
        print("  Global Search — Device Name")
        print("  Partial match — 'spine' matches abc-spine-01, dc-spine-02")
        divider()

        term = input(
            "  Enter search term or press Enter to return: "
        ).strip()

        if not term:
            return

        print(f"\n[*] Searching device names for: '{term}' ...")

        devices = api_get(
            url, token,
            "/api/dcim/devices/",
            params={"name__ic": term, "limit": 0},
        )

        if not devices:
            print(f"  [!] No devices found matching name: '{term}'")
            input("  Press Enter to search again...")
            continue

        summary_rows = display_device_summary(devices, term)

        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = summary_rows,
            fieldnames       = SUMMARY_FIELDS,
            default_filename = f"search_name_{term}_{timestamp}.csv",
        )

        search_again = drill_down_loop(
            url, token, devices, term,
            f"device_{term}_{timestamp}.csv",
        )
        if not search_again:
            return


def global_search_by_ip(url, token):
    """
    Search IP addresses using contains matching.
    Filter: address__ic=term
    Drill-down shows the device the IP belongs to.
    """
    while True:
        print("\n")
        divider()
        print("  Global Search — IP Address")
        print("  Partial match — '10.0.0' matches 10.0.0.1/32, 10.0.0.2/32")
        divider()

        term = input(
            "  Enter IP or partial IP or press Enter to return: "
        ).strip()

        if not term:
            return

        print(f"\n[*] Searching IP addresses for: '{term}' ...")

        ips = api_get(
            url, token,
            "/api/ipam/ip-addresses/",
            params={"address__ic": term, "limit": 0},
        )

        if not ips:
            print(f"  [!] No IP addresses found matching: '{term}'")
            input("  Press Enter to search again...")
            continue

        print(f"  [+] {len(ips)} IP address(es) found.\n")
        divider()
        print(f"  {'#':<5} {'IP Address':<25} {'Device':<30} "
              f"{'Interface':<25} {'Status'}")
        divider("-")

        ip_csv_rows = []
        for idx, ip in enumerate(ips, start=1):
            address   = ip.get("address", "N/A")
            status    = ip.get("status", {}).get("label", "N/A")
            obj       = ip.get("assigned_object")

            if obj:
                device_name = obj.get("device", {}).get("name", "N/A") \
                              if obj.get("device") else "N/A"
                iface_name  = obj.get("name", "N/A")
            else:
                device_name = "Unassigned"
                iface_name  = "N/A"

            print(f"  {idx:<5} {address:<25} {device_name:<30} "
                  f"{iface_name:<25} {status}")

            ip_csv_rows.append({
                "IP Address": address,
                "Device":     device_name,
                "Interface":  iface_name,
                "Status":     status,
            })

        divider()

        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = ip_csv_rows,
            fieldnames       = ["IP Address", "Device", "Interface", "Status"],
            default_filename = f"search_ip_{term}_{timestamp}.csv",
        )

        # Drill-down into device assigned to selected IP
        while True:
            print("\n  Enter a number to drill into the assigned device,")
            print("  's' to search again, or Enter to return to menu:")
            choice = input("  > ").strip().lower()

            if not choice:
                return

            if choice == "s":
                break   # back to outer search loop

            if not choice.isdigit() or not (1 <= int(choice) <= len(ips)):
                print(f"\n  [!] Invalid — enter a number between 1 and "
                      f"{len(ips)}.")
                continue

            selected_ip  = ips[int(choice) - 1]
            obj          = selected_ip.get("assigned_object")
            device_ref   = obj.get("device") if obj else None

            if not device_ref:
                print("  [!] This IP is not assigned to a device.")
                continue

            device_name = device_ref.get("name", "N/A")
            print(f"\n[*] Loading device detail for: {device_name} ...")

            matched = api_get(url, token, "/api/dcim/devices/",
                              params={"name": device_name})

            if not matched:
                print(f"  [!] Could not load device: {device_name}")
                continue

            print()
            detail_rows = display_device_detail(url, token, matched[0])

            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            prompt_save_csv(
                rows             = detail_rows,
                fieldnames       = DEVICE_DETAIL_FIELDS,
                default_filename = f"device_{device_name}_{timestamp}.csv",
            )

            input("\n  Press Enter to return to IP results...")

            # Redisplay IP list
            print(f"  [+] {len(ips)} IP address(es) found for '{term}'.\n")
            divider()
            print(f"  {'#':<5} {'IP Address':<25} {'Device':<30} "
                  f"{'Interface':<25} {'Status'}")
            divider("-")
            for idx, ip in enumerate(ips, start=1):
                address     = ip.get("address", "N/A")
                status      = ip.get("status", {}).get("label", "N/A")
                obj         = ip.get("assigned_object")
                device_name = obj.get("device", {}).get("name", "N/A") \
                              if obj and obj.get("device") else "Unassigned"
                iface_name  = obj.get("name", "N/A") if obj else "N/A"
                print(f"  {idx:<5} {address:<25} {device_name:<30} "
                      f"{iface_name:<25} {status}")
            divider()


def global_search_by_type_manufacturer(url, token):
    """
    Search devices by device type model OR manufacturer name.
    Fires two queries and deduplicates by device ID.
    Filters:
        device_type__model__ic=term
        manufacturer__name__ic=term
    """
    while True:
        print("\n")
        divider()
        print("  Global Search — Manufacturer")
        print("  Searches manufacturer name — Cisco, Arista, BlueCat, Ixia etc.")
        divider()

        term = input(
            "  Enter manufacturer name or press Enter to return: "
        ).strip()

        if not term:
            return

        print(f"\n[*] Searching device type and manufacturer for: '{term}' ...")

        # Single query — manufacturer name covers Cisco, Arista, BlueCat, Ixia etc.
        # manufacturer__name__ic confirmed working on this Nautobot instance
        by_type = api_get(
            url, token,
            "/api/dcim/devices/",
            params={"manufacturer__name__ic": term, "limit": 0},
        )

        # No second query needed — manufacturer name covers all use cases
        by_mfr = []

        # Deduplicate by device ID
        seen    = set()
        devices = []
        for device in (by_type + by_mfr):
            did = device.get("id")
            if did not in seen:
                seen.add(did)
                devices.append(device)

        if not devices:
            print(f"  [!] No devices found matching type/manufacturer: '{term}'")
            input("  Press Enter to search again...")
            continue

        print(f"  [+] {len(by_type)} match(es) by device type, "
              f"{len(by_mfr)} by manufacturer, "
              f"{len(devices)} unique after dedup.")

        summary_rows = display_device_summary(devices, term)

        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        prompt_save_csv(
            rows             = summary_rows,
            fieldnames       = SUMMARY_FIELDS,
            default_filename = f"search_type_{term}_{timestamp}.csv",
        )

        search_again = drill_down_loop(
            url, token, devices, term,
            f"device_{term}_{timestamp}.csv",
        )
        if not search_again:
            return


def query_global_search(url, token):
    """
    Global Search sub-menu.
    Three search types, each with partial matching built in.
    """
    while True:
        print("\n")
        divider()
        print("  Global Search")
        divider()
        print("  1. Search by Device Name")
        print("  2. Search by IP Address")
        print("  3. Search by Manufacturer")
        print("  4. Back to main menu")
        divider()

        choice = input("  Select an option: ").strip()

        if choice == "1":
            global_search_by_device_name(url, token)
