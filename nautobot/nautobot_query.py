# SHARED: DEVICE SUMMARY LIST + DRILL-DOWN
# Reused by both Global Search and Search by Device Name
# =======================================================

def display_summary_and_drill(url, token, devices, context_label=""):
    """
    Display a numbered summary list of devices then offer drill-down.

    Columns: #, Name, Status, Tenant, Role, Type, Site, Rack, Primary IP

    Args:
        url           : Nautobot base URL
        token         : API token
        devices       : List of device dicts from api_get()
        context_label : Optional label shown above the list (e.g. search term)

    Returns flat list of CSV-ready summary rows.
    """
    if context_label:
        print(f"\n  Results for: '{context_label}'")

    divider()
    print(f"  [+] {len(devices)} device(s) found.\n")
    print(f"  {'#':<5} {'Name':<30} {'Status':<10} {'Tenant':<15} "
          f"{'Role':<15} {'Type':<22} {'Site':<10} {'Rack':<15} {'Primary IP'}")
    divider("-")

    summary_csv_rows = []

    for idx, device in enumerate(devices, start=1):
        name       = device.get("name", "N/A")
        status     = device.get("status", {}).get("label", "N/A")

        tenant     = device.get("tenant")
        tenant     = tenant.get("name", "N/A") if tenant else "N/A"

        role       = device.get("role") or device.get("device_role")
        role       = role.get("name", "N/A") if role else "N/A"

        dtype      = device.get("device_type", {}).get("display", "N/A")

        site       = device.get("site")
        site       = site.get("name", "N/A") if site else "N/A"

        rack       = device.get("rack")
        rack       = rack.get("name", "N/A") if rack else "N/A"

        primary_ip = device.get("primary_ip")
        primary_ip = primary_ip.get("address", "N/A") if primary_ip else "N/A"

        print(f"  {idx:<5} {name:<30} {status:<10} {tenant:<15} "
              f"{role:<15} {dtype:<22} {site:<10} {rack:<15} {primary_ip}")

        summary_csv_rows.append({
            "Name":       name,
            "Status":     status,
            "Tenant":     tenant,
            "Role":       role,
            "Type":       dtype,
            "Site":       site,
            "Rack":       rack,
            "Primary IP": primary_ip,
        })

    divider()
    return summary_csv_rows


SUMMARY_FIELDS = [
    "Name", "Status", "Tenant", "Role", "Type", "Site", "Rack", "Primary IP"
]


