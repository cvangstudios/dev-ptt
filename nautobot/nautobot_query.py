def query_racks(url, token):
    """
    Stage 1 — Single API call pulls all racks at SITE_NAME.
              Displays a fast numbered summary list.
              CSV export available from summary.
    Stage 2 — User selects a rack number to see all devices in it.
              Additional API call made only for the selected rack.
              Devices sorted by RU position top to bottom.
              CSV export available from rack detail.
              Returns to rack list after — not main menu.
    """
